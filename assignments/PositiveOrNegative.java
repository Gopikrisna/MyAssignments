package week1.assignments;

public class PositiveOrNegative 
{

	public static void main(String[] args) 
	{
		int num = -9;
		if (num >= 0)
		{
			System.out.println("Positive Number");
		}
		else if (num < 0)
		{
			System.out.println("Negative Number");
		}
		else 
		{
			System.out.println("Value is Zero");	
		}

	}

}
